namespace MaterialForms
{
    /// <summary>
    ///     Represents the color theme of the displayed dialog.
    /// </summary>
    public enum DialogTheme
    {
        /// <summary>
        ///     Inherits the scheme used by the application.
        /// </summary>
        Inherit,

        /// <summary>
        ///     Light background with dark foreground color.
        /// </summary>
        Light,

        /// <summary>
        ///     Dark background with light foreground color.
        /// </summary>
        Dark
    }
}